// Current language (detect from browser or localStorage)
let currentLanguage = localStorage.getItem('ieg-language') || 
  (navigator.language.startsWith('de') ? 'de' : 'en');

// Get translation by key
function t(key, defaultValue = key) {
  const parts = key.split('.');
  let value = TRANSLATIONS[currentLanguage];
  
  for (let part of parts) {
    if (value && typeof value === 'object') {
      value = value[part];
    } else {
      return defaultValue;
    }
  }
  
  return value || defaultValue;
}

// Set language
function setLanguage(lang) {
  if (TRANSLATIONS[lang]) {
    currentLanguage = lang;
    localStorage.setItem('ieg-language', lang);
    updatePageLanguage();
  }
}

// Get current language
function getCurrentLanguage() {
  return currentLanguage;
}

// Update entire page language
function updatePageLanguage() {
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  
  // Update all elements with data-i18n-html attribute
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    el.innerHTML = t(key);
  });
  
  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });
  
  // Update page direction for RTL languages (if needed in future)
  document.documentElement.lang = currentLanguage;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
  updatePageLanguage();
});
